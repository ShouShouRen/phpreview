<?php

$a = 10;
$b = 5;

//算術運算子

echo $a + $b;
echo "<br>";
echo $a - $b;
echo "<br>";
echo $a * $b;
echo "<br>";
echo $a / $b;
echo "<br>";
echo $a % $b;   // 取餘數   二數相除後，抓取餘數
echo "<br>";

echo 10 / 3;
echo "<br>";

// ++ 遞增 每次加1    -- 遞減  每次減1

echo $a++;   // 若++ 在變數後 ，則先將值印出來後，再自己作 + 1
echo "<br>";
echo --$b;   // -- 在變數之前，則會先運算 -1  後，再將值印出來
echo "<br>";




?>